# Example of while loop with user's input
def count_to_five():
    # Initializing a loop control variable
    count = 1

    # Looping until count reaches 6 (inclusive)
    while count <= 5:
        # Printing the current count
        print("Count:", count)

        # Incrementing the count
        count += 1


# Executing the function
count_to_five()

